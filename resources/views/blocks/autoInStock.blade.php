<script>
    document.addEventListener("DOMContentLoaded", () => {
        const initializeSwipers = () => {
            const cardSliders = document.querySelectorAll(".autoInStock .inStockCard .inStockCardSlider");
            cardSliders.forEach((cardSlider) => {
                new Swiper(cardSlider, {
                    direction: "horizontal",
                    allowTouchMove: true,
                    // loop: true,
                    pagination: {
                        el: ".swiper-pagination",
                        clickable: true,
                        renderBullet: function (index, className) {
                            return `<div title="Увеличить" class="${className}" data-target="${index}"></div>`;
                        },
                    },
                    on: {
                        afterInit: (swiper) => {
                            [...cardSlider.querySelectorAll(".swiper-pagination .swiper-pagination-bullet"),].forEach((bullet) => {
                                // console.log(bullet)
                                // bullet.addEventListener("mouseenter", () => {
                                //     if(window.innerWidth > 992){
                                //         swiper.slideTo(Number(bullet.getAttribute("data-target")), 100);
                                //     }
                                // });
                                bullet.addEventListener("click", () => {
                                    fsLightboxInstances[cardSlider.querySelector(".swiper-slide [data-fslightbox]").getAttribute("data-fslightbox")].open(Number(bullet.getAttribute("data-target")));
                                });
                            });
                        },
                    },
                });
            });
        };

        const stockCards = document.querySelectorAll(".inStockCardMain");
        const showMoreButton = document.querySelector(".autoInStockMore");
        const modelSelector = document.getElementById("selectModel");
        let cardIncrement = window.innerWidth < 992 ? 2 : 3; // по сколько моделей отображать по кнопке
        let visibleCardCount = 6; // Изначальное количество видимых карточек

        const extractUniqueModels = () => {
            const modelsSet = new Set();
            stockCards.forEach((card) => {
                const cardHeaderElement = card.querySelector(".inStockCardHeaderMain");
                if (cardHeaderElement) {
                    const cardHeader = cardHeaderElement.textContent;
                    const model = cardHeader.split(",")[0].trim();
                    modelsSet.add(model);
                }
            });

            modelSelector.innerHTML = '<option value="0" selected>Выберите модель</option>';

            modelsSet.forEach((model) => {
                const option = document.createElement("option");
                option.value = model;
                option.textContent = model;
                modelSelector.appendChild(option);
            });
        };

        const displayFilteredCards = () => {
            const selectedModel = modelSelector.value;
            let filteredCards = [];

            stockCards.forEach((card) => {
                const cardHeaderElement = card.querySelector(".inStockCardHeaderMain");
                if (cardHeaderElement) {
                    const cardHeader = cardHeaderElement.textContent;
                    const model = cardHeader.split(",")[0].trim();
                    if (selectedModel === "0" || model === selectedModel) {
                        filteredCards.push(card);
                    } else {
                        card.classList.add("d-none");
                    }
                }
            });

            filteredCards.forEach((card, index) => {
                if (index < visibleCardCount) {
                    card.classList.remove("d-none");
                } else {
                    card.classList.add("d-none");
                }
            });

            showMoreButton.classList.toggle(
                "d-none",
                visibleCardCount >= filteredCards.length
            );

            initializeSwipers(); // Переинициализация Swiper для вновь отображенных карточек
        };

        showMoreButton.addEventListener("click", () => {
            visibleCardCount += cardIncrement; // Увеличиваем количество видимых карточек
            displayFilteredCards();
        });

        modelSelector.addEventListener("change", () => {
            visibleCardCount = 6; // Сбрасываем количество видимых карточек при смене фильтра
            displayFilteredCards();
        });

        extractUniqueModels();
        displayFilteredCards();
    });
</script>
